								<h2>3R´s</h2>
								<p>
									En Ingrup mantenemos una cultura de mejora ambiental, aplicando una estrategia básica, la ley de las 3R´s:  
									<strong>Reducir, reutilizar y reciclar.</strong>
									<br/><br/>
									<strong>Reducir:</strong> Consiste en evitar todo aquello que genera un desperdicio innecesario.
									¿Cómo puedo reducir?  Detectar y reparar fugas de agua, apagar luces innecesarias o equipos que no se están
									utilizando, y consumir sólo lo que se necesite.  Debemos hacer énfasis especial en el aprovechamiento de la 
									energía eléctrica, que representa más del 90% de la huella de carbono de los procesos de Ingrup
									Como resultado del trabajo en equipo con clientes y proveedores, reducir pesos de los empaques, envases y
									embalajes.
									<br/><br/>
									<strong>Reutilizar:</strong> Volver a utilizar un producto o material varias veces para prolongarle su vida útil, ya sea 
									reparándolos para un mismo uso, o con creatividad para un uso diferente.
									¿Cómo puedo reutilizar?  Utilizar aquellos recursos que puedan volver a usarse. Por ejemplo, aprovechamiento
									del material de embalaje y de estiba durante varios ciclos de uso. 
									En muchos casos la energía y los recursos son reutilizables, sacándoles el mayor provecho. Por ejemplo 
									reutilización del aire comprimido en subprocesos, y del agua tras su debido tratamiento.
									Con materiales que fueron utilizados para su uso original, se pueden elaborar otros nuevos. Lo que no le 
									sirve a uno, puede ser útil a alguien más.
									<br/><br/>
									<strong>Reciclar:</strong> es el proceso mediante el cual los materiales son recolectados, transformados y utilizados como 
									materia prima para nuevos productos.  
									El reciclaje previene que materiales lleguen a los rellenos sanitarios o sean quemados, reduciendo la 
									contaminación y los volúmenes destinados a los sitios de disposición final.  Así mismo, el reciclaje reduce 
									el consumo de energía y de recursos nuevos.
									Un tercer impacto positivo del reciclaje es el desarrollo socioeconómico, mediante la generación de empleo, 
									tanto en la fase de acopio, como en la de la industrialización y comercialización.
								</p>
